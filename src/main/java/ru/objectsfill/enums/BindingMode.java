package ru.objectsfill.enums;

/**
 * Режим привязки колонок при чтении loop-объектов.
 * Применяется только к ключам с префиксом {@code "for."}.
 * На одиночные объекты не влияет — они всегда читаются по позиции.
 */
public enum BindingMode {

    /**
     * Привязка по заголовку.
     * Библиотека ищет в import-файле ячейку с текстом, совпадающим с заголовком
     * над маркером в шаблоне ({@code headerName}), и привязывается к её колонке.
     * Позволяет читать данные корректно даже если колонки переставлены местами.
     * Поведение по умолчанию.
     */
    HEADER,

    /**
     * Привязка по позиции.
     * Библиотека использует координаты маркера в шаблоне напрямую.
     * Import-файл должен иметь ту же структуру колонок, что и шаблон.
     * Заголовки в import-файле не ищутся и не проверяются.
     */
    POSITION
}
